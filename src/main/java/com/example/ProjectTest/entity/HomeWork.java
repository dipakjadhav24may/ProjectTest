package com.example.ProjectTest.entity;

import java.util.Date;

public class HomeWork {
	
	private String homework_name;
	
	private String homework_topic;
	
	private String folder_name;
	
	private int batch_id;
	
	private int class_id;
	
	private int subject_id;
	
	private Date start_date;
	
	private Date end_date;
	
	private int total_marks;

	public String getHomework_name() {
		return homework_name;
	}

	public void setHomework_name(String homework_name) {
		this.homework_name = homework_name;
	}

	public String getHomework_topic() {
		return homework_topic;
	}

	public void setHomework_topic(String homework_topic) {
		this.homework_topic = homework_topic;
	}

	public String getFolder_name() {
		return folder_name;
	}

	public void setFolder_name(String folder_name) {
		this.folder_name = folder_name;
	}

	public int getBatch_id() {
		return batch_id;
	}

	public void setBatch_id(int batch_id) {
		this.batch_id = batch_id;
	}

	public int getClass_id() {
		return class_id;
	}

	public void setClass_id(int class_id) {
		this.class_id = class_id;
	}

	public int getSubject_id() {
		return subject_id;
	}

	public void setSubject_id(int subject_id) {
		this.subject_id = subject_id;
	}

	public Date getStart_date() {
		return start_date;
	}

	public void setStart_date(Date start_date) {
		this.start_date = start_date;
	}

	public Date getEnd_date() {
		return end_date;
	}

	public void setEnd_date(Date end_date) {
		this.end_date = end_date;
	}

	public int getTotal_marks() {
		return total_marks;
	}

	public void setTotal_marks(int total_marks) {
		this.total_marks = total_marks;
	}
	
	
	

}
